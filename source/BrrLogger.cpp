// system
#include <errno.h>

//! ************************************************************************************************
//!
//! ************************************************************************************************
std::string brr::StrErrno(int error)
{
    const size_t c_bufferLen   = 256;
    char buffer[ c_bufferLen ] = { 0 };
    return strerror_r(error, buffer, c_bufferLen);
}

//! ************************************************************************************************
//!
//! ************************************************************************************************
std::string brr::StrErrno()
{
    return StrErrno( errno );
}
